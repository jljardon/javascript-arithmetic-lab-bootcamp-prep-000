function add(a,b){
  var result = a + b
  return result
}

function substract(a,b){
  var result = a - b
  return result
}

function multiply(a,b){
  var result = a * b
  return result
}

function divide(a,b){
  var result = a / b
  return result
}

function inc(a){
  var result = a++
  return result
}

